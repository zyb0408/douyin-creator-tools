#!/bin/bash

# =========================================================
# 抖音创作者自动化全流程脚本
# 一键执行：获取作品 → 导出评论 → 生成回复 → 自动回复 → 清理中间文件
# =========================================================

set -e # 遇到错误立即退出

echo "🚀 开始执行抖音创作者自动化全流程..."

# 1. 获取作品列表
echo "[1/4] 正在获取最新作品列表..."
npm run works -- --limit 8
if [ $? -ne 0 ]; then
    echo "❌ 获取作品列表失败！"
    exit 1
fi

# 2. 导出所有作品的未回复评论
echo "[2/4] 正在导出所有作品的未回复评论..."
npm run comments:export
if [ $? -ne 0 ]; then
    echo "❌ 导出评论失败！"
    exit 1
fi

# 3. 生成AI回复（自动追加签名）
echo "[3/4] 正在使用大模型生成回复内容..."
npm run comments:generate-reply
if [ $? -ne 0 ]; then
    echo "❌ 生成回复失败！"
    exit 1
fi

# 4. 自动回复所有作品的评论
echo "[4/4] 正在自动回复所有作品的评论..."
npm run comments:reply-all
if [ $? -ne 0 ]; then
    echo "❌ 自动回复失败！"
    exit 1
fi

echo "\n🎉 全流程执行成功！所有中间文件已自动清理。"
echo "✅ 您的抖音评论回复任务已完成。"
